import java.util.Scanner;

public class SumoFNumber {
    public static void main(String[] args){

        Scanner scan = new Scanner(System.in);
        System.out.println("Enter First N Number ");
        int j = scan.nextInt();
        int a = 0;
        for (int i = 0; i < j; ++i){
            System.out.println(a +=i);
        }

    }
}
